function crearTarjetas(categoria, contenedorId) {
    const contenedor = document.getElementById(contenedorId);
    let htmlFinal = "";
    datosJuegos[categoria].forEach(juego => {
        htmlFinal += `
            <div class="game-card">
                <img src="${juego.img}" alt="${juego.titulo}" loading="lazy">
                <div class="game-info">
                    <div class="title-box">${juego.titulo}</div>
                    <div class="description-box">${juego.desc}</div>
                </div>
                <div class="video-box">
                    <iframe src="${juego.video}" frameborder="0" allowfullscreen></iframe>
                </div>
            </div>`;
    });
    contenedor.innerHTML = htmlFinal;
}

